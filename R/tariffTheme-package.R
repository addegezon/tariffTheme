#' tariffTheme: Custom Themed Visualizations for Tariff Data
#'
#' @description
#' The tariffTheme package provides a consistent visual style for creating
#' tariff data visualizations. It includes a custom ggplot2 theme, color palettes,
#' and utility functions for adding logos and styling elements.
#'
#' @section Main Functions:
#' \itemize{
#'   \item \code{\link{theme_tariff}}: Apply the tariff theme to a ggplot object
#'   \item \code{\link{add_logo}}: Add a logo and styled borders to a plot
#'   \item \code{\link{scale_color_tariff}}: Use the tariff theme color palette for color aesthetics
#'   \item \code{\link{scale_fill_tariff}}: Use the tariff theme color palette for fill aesthetics
#'   \item \code{\link{style_tariff_label}}: Apply consistent styling to labels and annotations
#' }
#'
#' @section Example Dataset:
#' The package includes the \code{\link{country_tariffs}} dataset which contains
#' reciprocal tariff rates for various countries that can be used for examples.
#'
#' @keywords internal
